namespace ForikAuction.Game;

/// <summary>Описание таланта в дереве. Стоимость растёт с уровнем (CostPerLevel[level-1]).</summary>
public sealed record TalentDef(
    string Code, string Branch, string Name, string Description,
    int MaxLevel, int[] CostPerLevel, string? RequiresCode, int RequiresLevel);

/// <summary>
/// Дерево талантов. Прокачка идёт за «очки таланта» (ОТ), которые копятся каждый аукцион.
/// ВСЕ эффекты ЛИЧНЫЕ: они меняют исход/очки только того игрока, который прокачал талант,
/// а не всех в комнате. Стоимость уровней РАСТЁТ — выгоднее качать вширь, а не в одну ветку.
/// </summary>
public static class TalentCatalog
{
    public static readonly IReadOnlyList<TalentDef> All = new List<TalentDef>
    {
        // Ветка БОГАТСТВО — прямая сила ставки.
        new("capital", "Богатство", "Капитал",
            "+40 базовых очков на каждый аукцион за уровень.",
            5, new[]{3,5,8,12,18}, null, 0),
        new("investor", "Богатство", "Инвестор",
            "В конце аукциона неистраченные очки конвертируются в ОТ (10/15/20%). Спорный выбор: тратить очки на ставку или копить на прокачку.",
            3, new[]{6,10,16}, "capital", 2),

        // Ветка СОПРОТИВЛЕНИЕ — управление баффом/дебаффом исхода.
        new("stipend", "Сопротивление", "Стипендия неудачника",
            "Лично тебе: если ТЫ проиграешь — получишь больше очков на следующий аукцион. +50 за уровень (твой бонус 200 -> 250 -> 300 -> 350). У соперника бонус не меняется.",
            3, new[]{4,7,11}, null, 0),
        new("nobility", "Сопротивление", "Благородство",
            "Лично тебе: если ТЫ выиграешь — потеряешь меньше. -50 к твоему штрафу за уровень (200 -> 150 -> 100 -> 50). Соперника не касается.",
            3, new[]{4,7,11}, null, 0),

        // Ветка КВЕСТЫ — больше очков из реальных дел.
        new("curiosity", "Квесты", "Пытливый ум",
            "+1 квест на следующий аукцион за уровень (4 -> 5 -> 6).",
            2, new[]{5,9}, null, 0),
        new("motivation", "Квесты", "Мотивация",
            "+15% к награде за выполненные квесты за уровень.",
            3, new[]{4,7,11}, null, 0),

        // Ветка УДАЧА — личный множитель шанса на колесе.
        new("luck", "Удача", "Фартовый",
            "+3% к твоему весу на колесе за уровень (шанс чуть выше, чем по чистым очкам).",
            3, new[]{6,10,16}, null, 0),
    };

    public static TalentDef Get(string code) => All.First(t => t.Code == code);

    /// <summary>Стоимость следующего уровня (или null, если уже максимум).</summary>
    public static int? CostForNextLevel(string code, int currentLevel)
    {
        var d = Get(code);
        if (currentLevel >= d.MaxLevel) return null;
        return d.CostPerLevel[currentLevel];
    }
}
