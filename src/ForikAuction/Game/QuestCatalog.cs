    public static QuestDef Get(int id) => All.First(q => q.Id == id);

    /// <summary>Выдать N случайных квестов на следующий аукцион.</summary>
    public static IReadOnlyList<QuestDef> Draw(int count, Random rng)
    {
        var pool = All.ToList();
        var picked = new List<QuestDef>();
        count = Math.Min(count, pool.Count);
        for (int i = 0; i < count; i++)
        {
            int k = rng.Next(pool.Count);
            picked.Add(pool[k]);
            pool.RemoveAt(k);
        }
        return picked;
    }
